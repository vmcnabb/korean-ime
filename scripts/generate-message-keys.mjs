import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const sourcePath = path.join(repoRoot, "src", "_locales", "en", "messages.json");
const outputPath = path.join(repoRoot, "src", "i18n", "message-key.ts");
const checkOnly = process.argv.includes("--check");

const messages = JSON.parse(await readFile(sourcePath, "utf8"));
const keys = Object.keys(messages).sort();

const union = keys.map((key) => `    | ${JSON.stringify(key)}`).join("\n");
const contents = `// Generated by scripts/generate-message-keys.mjs from src/_locales/en/messages.json.
// Do not edit by hand.

export type MessageKey =
${union};
`;

if (checkOnly) {
    let existing = "";
    try {
        existing = await readFile(outputPath, "utf8");
    } catch (error) {
        if (error.code !== "ENOENT") {
            throw error;
        }
    }

    if (existing !== contents) {
        console.error("[gen-message-keys] src/i18n/message-key.ts is stale. Run `npm run gen-message-keys`.");
        process.exit(1);
    }

    console.log(`[gen-message-keys] OK — ${keys.length} keys`);
    process.exit(0);
}

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, contents);
console.log(`[gen-message-keys] wrote ${path.relative(repoRoot, outputPath)} with ${keys.length} keys`);
